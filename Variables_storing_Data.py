# Pseudocode:
# 1. Ask the user for their name
# 2. Ask the user for their age
# 3. Ask the user for their house number
# 4. Ask the user for their street name
# 5. Print a sentence containing all the details of the user

# Python code:

# Ask the user for their name
name = input("Please enter your name: ")

# Ask the user for their age
age = input("Please enter your age: ")

# Ask the user for their house number
house_number = input("Please enter your house number: ")

# Ask the user for their street name
street_name = input("Please enter your street name: ")

# Print a sentence containing all the details of the user
print(f"This is {name}.They are {age} years old and live at house number\
       {house_number} on {street_name}.")
